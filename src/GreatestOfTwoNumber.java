public class GreatestOfTwoNumber {

    //Step 1: Initialize Variables:

    //Set num1 to 32.
    //Set num2 to 31.

    //Step 2: Comparison and Output:

    //Check if num1 is greater than num2.
    //If true, print num1 with the message " is the Greatest Number".
    //If false, print num2 with the message " is the Greatest Number".

    //End the program.

    public static void main(String[] args)
    {
        int num1 = 32;
        int num2 = 31;

        if(num1 > num2)
        {
            System.out.println(num1+" is the Greatest Number");
        }
        else
        {
            System.out.println(num2+" is the Greatest Number");
        }

    }
}
